class NutritionMatcher:
    """营养信息匹配器"""
    
    def __init__(self, db_path: Path):
        """
        初始化匹配器
        
        Args:
            db_path: 营养数据库路径 (food.db)
        """
        self.db_path = db_path
        self._load_database()
    
    def _parse_value(self, raw) -> float:
        """解析带单位的营养值文本
        
        '1698kJ' → 1698.0 | '0.1g' → 0.1 | '—'/'Tr'/'' → 0.0
        """
        if not raw or str(raw).strip() in ('—', 'Tr', '─', '…'):
            return 0.0
        return float(re.sub(r'[a-zA-Zμ%]+$', '', str(raw).strip()))
    
    def _load_database(self):
        """加载新营养数据库（food_nutrition.db）"""
        conn = sqlite3.connect(self.db_path)
        cursor = conn.cursor()
        
        cursor.execute(
            "SELECT id, food_name, energy, protein, fat, carbohydrate "
            "FROM j_food_nutrition"
        )
        rows = cursor.fetchall()
        
        self._food_map = {}     # food_name → (id, food_name, energy_kcal, protein, fat, carbs)
        self._food_names = []   # 用于模糊匹配
        self._alias_to_main = {}  # 别名 → 主名
        
        for row in rows:
            id_, food_name, energy_raw, protein_raw, fat_raw, carbs_raw = row
            
            # 跳过重复的食物名（仅保留第一个）
            if food_name in self._food_map:
                continue
            
            # 解析带单位的营养值，能量 kJ → kcal
            energy_kj = self._parse_value(energy_raw)
            energy_kcal = round(energy_kj / 4.184, 1)
            protein = round(self._parse_value(protein_raw), 1)
            fat = round(self._parse_value(fat_raw), 1)
            carbs = round(self._parse_value(carbs_raw), 1)
            
            parsed = (id_, food_name, energy_kcal, protein, fat, carbs)
            self._food_map[food_name] = parsed
            self._food_names.append(food_name)
            
            # 从 food_name 中的 [别名] 提取别名映射
            alias_match = re.search(r'\[([^\]]+)\]', food_name)
            if alias_match:
                aliases = alias_match.group(1)
                for alias in re.split(r'[、，,]', aliases):
                    alias = alias.strip()
                    if alias:
                        self._alias_to_main[alias] = food_name
            
            # 去掉所有括号后的纯名也作为别名
            pure_name = re.sub(r'[\(\[（【][^\)\]）】]*[\)\]）】]', '', food_name).strip()
            if pure_name and pure_name != food_name:
                self._alias_to_main[pure_name] = food_name
        
        conn.close()
        print(f"[NutritionMatcher] 加载 {len(self._food_map)} 种食物，{len(self._alias_to_main)} 个别名")
    
    def _normalize_name(self, name: str) -> str:
        """规范化食材名称"""
        if not name:
            return ''
        
        # 保护特定名称不被修改
        protected_names = {
            '味极鲜', '老干妈', '消化饼干', '手指饼干', '奥利奥饼干',
            '生抽', '老抽', '老姜', '生姜', '干贝', '干辣椒',
            '干香菇', '鲜虾', '鲜鱼', '鲜牛奶', '鲜奶油',
            '干酵母', '耐高糖酵母', '干木耳', '干百合',
            '老豆腐', '鲜豆腐', '老面', '鲜花椒',
            '内酯豆腐',
        }
        if name in protected_names:
            return name
        
        # 去除明确的前缀修饰词
        name = re.sub(r'^(新鲜|有机|进口|国产|优质|精选|特级|一级|水发)', '', name)
        # 统一全角括号为半角，然后去除括号内容
        name = name.replace('（', '(').replace('）', ')')
        name = name.replace('【', '[').replace('】', ']')
        name = re.sub(r'[\(\[].+?[\)\]]', '', name)
        name = name.strip()
        return name
    
    def match(self, ingredient_name: str, quantity_grams: float = None) -> Optional[NutritionInfo]:
        """
        匹配食材并返回营养信息
        
        匹配优先级：
        1. COMMON_SEASONINGS（调味品/香料，用量小可忽略）
        2. COMBO_SPLITS（组合词拆分后取平均）
        3. MANUAL_NUTRITION / MANUAL_ALIASES（手工补充的数据库外营养数据）
        4. ALIAS_MAP（别名映射到数据库）
        5. 数据库精确匹配
        6. 数据库别名匹配（从name字段中的[]提取）
        7. 数据库包含匹配
        8. 数据库模糊匹配
        9. 后缀剥离回退（去掉切割后缀再尝试）
        
        Args:
            ingredient_name: 食材名称
            quantity_grams: 数量（克），用于计算实际营养值
            
        Returns:
            NutritionInfo 或 None
        """
        if not ingredient_name:
            return None
        
        name = self._normalize_name(ingredient_name)
        if not name:
            return None
        
        # 1. 检查 COMMON_SEASONINGS（调味品/香料）
        if name in COMMON_SEASONINGS:
            energy, protein, fat, carbs = COMMON_SEASONINGS[name]
            return NutritionInfo(
                id=-1,
                name=name,
                energy=energy,
                protein=protein,
                fat=fat,
                carbs=carbs,
                match_score=100
            )
        
        # 2. 组合词拆分（葱姜蒜→取各成分平均值）
        if name in COMBO_SPLITS:
            components = COMBO_SPLITS[name]
            if components:
                results = []
                for comp in components:
                    r = self.match(comp)
                    if r:
                        results.append(r)
                if results:
                    avg_energy = round(sum(r.energy for r in results) / len(results), 1)
                    avg_protein = round(sum(r.protein for r in results) / len(results), 1)
                    avg_fat = round(sum(r.fat for r in results) / len(results), 1)
                    avg_carbs = round(sum(r.carbs for r in results) / len(results), 1)
                    return NutritionInfo(
                        id=-2,
                        name=f"{name}[{'+'.join(components)}]",
                        energy=avg_energy,
                        protein=avg_protein,
                        fat=avg_fat,
                        carbs=avg_carbs,
                        match_score=100
                    )
        
        # 3. 手工补充营养数据（数据库不存在的食材）
        manual_key = MANUAL_ALIASES.get(name, name)
        if manual_key in MANUAL_NUTRITION:
            energy, protein, fat, carbs = MANUAL_NUTRITION[manual_key]
            return NutritionInfo(
                id=-3,
                name=manual_key,
                energy=energy,
                protein=protein,
                fat=fat,
                carbs=carbs,
                match_score=100
            )
        
        # 4. 检查 ALIAS_MAP
        if name in ALIAS_MAP:
            db_name = ALIAS_MAP[name]
            if db_name in self._food_map:
                id_, food_name, energy, protein, fat, carbs = self._food_map[db_name]
                return NutritionInfo(
                    id=id_,
                    name=food_name,
                    energy=energy,
                    protein=protein,
                    fat=fat,
                    carbs=carbs,
                    match_score=100
                )
        
        # 5. 数据库精确匹配
        if name in self._food_map:
            id_, food_name, energy, protein, fat, carbs = self._food_map[name]
            return NutritionInfo(
                id=id_,
                name=food_name,
                energy=energy,
                protein=protein,
                fat=fat,
                carbs=carbs,
                match_score=100
            )
        
        # 6. 数据库别名匹配（从name字段中的[]提取的别名）
        if name in self._alias_to_main:
            main_name = self._alias_to_main[name]
            if main_name in self._food_map:
                id_, food_name, energy, protein, fat, carbs = self._food_map[main_name]
                return NutritionInfo(
                    id=id_,
                    name=food_name,
                    energy=energy,
                    protein=protein,
                    fat=fat,
                    carbs=carbs,
                    match_score=100
                )
        
        # 7. 包含匹配（数据库名称包含输入名称）
        for db_name, row in self._food_map.items():
            db_main_name = re.sub(r'\[.+\]', '', db_name).strip()
            db_main_name = re.sub(r'\(.+\)', '', db_main_name).strip()
            
            if name == db_main_name or db_main_name == name:
                id_, food_name, energy, protein, fat, carbs = row
                return NutritionInfo(
                    id=id_,
                    name=food_name,
                    energy=energy,
                    protein=protein,
                    fat=fat,
                    carbs=carbs,
                    match_score=95
                )
        
        # 8. 后缀剥离回退（去掉切割/加工后缀再尝试匹配）
        _CUTTING_SUFFIXES = ('丝', '丁', '片', '块', '段', '末', '碎', '粒', '条', '泥', '汁')
        if len(name) >= 3:
            for suffix in _CUTTING_SUFFIXES:
                if name.endswith(suffix):
                    stripped = name[:-1]
                    # 依次尝试 ALIAS_MAP、DB精确、DB别名
                    if stripped in ALIAS_MAP:
                        db_name = ALIAS_MAP[stripped]
                        if db_name in self._food_map:
                            id_, food_name, energy, protein, fat, carbs = self._food_map[db_name]
                            return NutritionInfo(
                                id=id_, name=food_name,
                                energy=energy, protein=protein, fat=fat, carbs=carbs,
                                match_score=90
                            )
                    if stripped in self._food_map:
                        id_, food_name, energy, protein, fat, carbs = self._food_map[stripped]
                        return NutritionInfo(
                            id=id_, name=food_name,
                            energy=energy, protein=protein, fat=fat, carbs=carbs,
                            match_score=90
                        )
                    if stripped in self._alias_to_main:
                        main_name = self._alias_to_main[stripped]
                        if main_name in self._food_map:
                            id_, food_name, energy, protein, fat, carbs = self._food_map[main_name]
                            return NutritionInfo(
                                id=id_, name=food_name,
                                energy=energy, protein=protein, fat=fat, carbs=carbs,
                                match_score=90
                            )
                    break  # 只尝试一个后缀
        
        # 9. 模糊匹配
        matches = process.extract(
            name, 
            self._food_names, 
            scorer=fuzz.ratio,
            limit=3
        )
        
        if matches and matches[0][1] >= 60:
            best_match = matches[0][0]
            score = matches[0][1]
            
            if best_match in self._food_map:
                id_, food_name, energy, protein, fat, carbs = self._food_map[best_match]
                return NutritionInfo(
                    id=id_,
                    name=food_name,
                    energy=energy,
                    protein=protein,
                    fat=fat,
                    carbs=carbs,
                    match_score=score
                )
        
        # 未匹配
        return None
    
    def batch_match(self, ingredients: list) -> dict:
        """
        批量匹配食材
        
        Args:
            ingredients: 食材名称列表
            
        Returns:
            {name: NutritionInfo or None}
        """
        results = {}
        for name in ingredients:
            results[name] = self.match(name)
        return results


